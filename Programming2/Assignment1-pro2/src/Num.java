public class Num {

	String ones;
	String tens;
	String hundreds;
	
	
	public Num(String ones, String tens, String hundreds) {
		
		this.ones = ones;
		this.tens = tens;
		this.hundreds = hundreds;
	}


	public String getOnes() {
		return ones;
	}


	public void setOnes(String ones) {
		this.ones = ones;
	}


	public String getTens() {
		return tens;
	}


	public void setTens(String tens) {
		this.tens = tens;
	}


	public String getHundreds() {
		return hundreds;
	}


	public void setHundreds(String hundreds) {
		this.hundreds = hundreds;
	}
	








	
}
